def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def subtraction(numero1, numero2):
    return numero1 - numero2

def addition(numero1, numero2):
    return numero1 + numero2

def mult(numero1, numero2):
    return numero1 * numero2

def divide(numero1, numero2):
    return numero1 / numero2

